package day04;

public class ForEx01 {

	public static void main(String[] args) {
		
		//1~100까지의 합
		int sum = 0;
		for(int i = 0; i < 101; i++) {
			sum += i;
		}
		System.out.println(sum);
		System.out.println("=============================");
		
		for(int i = 10; i > 0;i--) {
			System.out.println(i);
		}
		
		
	}
}







































